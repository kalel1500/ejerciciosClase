    public Double tempVacia(String text) {
        boolean pasa = false;
        Double temp = 0.0;
        try {
            temp = Double.valueOf(text);
        } catch (Exception e) {
            while (!pasa) {
                JOptionPane.showMessageDialog(null, "No estas introduciendo un numero valido");
                try {
                    String resJO = JOptionPane.showInputDialog("Introduce la temperatura");
                    //if (resJO != null) {
                        temp = Double.valueOf(resJO);
                        pasa = true;
                    //} else {
                        pasa = true;
                    //}
                } catch (Exception ex) {
                    pasa = false;
                }
            }
        }
        return temp;
    }

