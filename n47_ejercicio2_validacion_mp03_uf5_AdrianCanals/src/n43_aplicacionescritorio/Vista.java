    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed
        // ocultar errores
        ocultarErrores();
        Double temperatura;
        
        if (this.rb_CtoF.isSelected() != true && this.rb_FtoC.isSelected() != true) {
            this.error_conversion.setVisible(true);
        } else {
            if (this.rb_CtoF.isSelected() == true) {
                //JOptionPane.showMessageDialog(rootPane, "c to f");
                if (tf_celsius.getText().equals("")) {
                    this.error_celsius.setVisible(true);
                } else {
                    temperatura = tempVacia(this.tf_celsius.getText());
                    String res = medida.calularMedida("fahrenheit", temperatura);
                    medida.setFahrenheit(res);
                    medida.setCelsius(this.tf_celsius.getText());
                } 
            }

            if (this.rb_FtoC.isSelected() == true) {
                //JOptionPane.showMessageDialog(rootPane, "f to c");
                if (tf_fahrenheit.getText().equals("")) {
                    this.error_fahrenheit.setVisible(true);
                } else {
                    temperatura = tempVacia(this.tf_fahrenheit.getText());
                    String res = medida.calularMedida("celsius", temperatura);
                    medida.setCelsius(res);
                    medida.setFahrenheit(this.tf_fahrenheit.getText());
                }
            }

            llenarCampos();
        }
    }//GEN-LAST:event_btn_calcularActionPerformed

